public class BinmodModelDataMesh
{
    public string Name { get; set; }
    public uint[,] FaceIndices { get; set; }
    public List<BinmodVector3> VertexPositions { get; set; } = new();
    public List<BinmodNormal> Normals { get; set; } = new();
    public List<BinmodNormal> Tangents { get; set; } = new();
    public List<BinmodUVMap32> UVMaps { get; set; } = new();
    public List<BinmodColorMap> ColorMaps { get; set; } = new();
    public List<List<ushort[]>> WeightIndices { get; set; } = new();
    public List<List<int[]>> WeightValues { get; set; } = new();
    public MaterialData Material { get; set; }
    public BlenderMaterialData BlenderMaterial { get; set; }
    public List<BinmodMeshPart> MeshParts { get; set; }
    public List<BinmodPartsLayer> PartsLayers { get; set; }

    public void UpdateForPartsSystem()
    {
        var faces = new Dictionary<uint, uint[]>();
        for (var i = 0u; i < FaceIndices.Length / 3; i++)
        {
            var tri = new uint[3];
            for (var j = 0; j < 3; j++)
            {
                tri[j] = FaceIndices[i, j];
            }

            faces.Add(i, tri);
        }

        var partsBase = PartsLayers.FirstOrDefault(p => p.Name == "Parts_Base");
        if (partsBase == null)
        {
            partsBase = new BinmodPartsLayer {Name = "Parts_Base", Faces = new List<uint>()};
            PartsLayers.Add(partsBase);
        }

        foreach (var (index, _) in faces)
        {
            if (!PartsLayers.Any(p => p.Faces.Any(f => f == index)))
            {
                partsBase.Faces.Add(index);
            }
        }

        var ordered = faces.OrderBy(kvp => partsBase.Faces.Any(f => f == kvp.Key));
        foreach (var layer in PartsLayers.Where(p => p != partsBase))
        {
            ordered = ordered.ThenBy(kvp => layer.Faces.Any(f => f == kvp.Key));
        }

        var result = ordered.Select(kvp => (Index: kvp.Key, VertexIndices: kvp.Value)).ToList();

        MeshParts = new List<BinmodMeshPart>();
        foreach (var layer in PartsLayers.Where(p => p.Faces.Count > 0))
        {
            MeshParts.Add(new BinmodMeshPart
            {
                StartIndex = (uint)result.IndexOf(result.FirstOrDefault(r => layer.Faces.Any(f => f == r.Index))) * 3u,
                IndexCount = (uint)result.Count(r => layer.Faces.Contains(r.Index)) * 3u,
                PartsId = PartsId.Dictionary[layer.Name]
            });
        }

        for (var i = 0; i < result.Count; i++)
        {
            for (var j = 0; j < 3; j++)
            {
                FaceIndices[i, j] = result[i].VertexIndices[j];
            }
        }
    }
}

